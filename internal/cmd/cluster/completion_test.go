// Completion tests invoke the binary with "__complete" as the subcommand.
// This is not a shell convention — it is a Cobra internal: the shell completion
// script generated by "qcloud completion <shell>" calls "__complete <args...>"
// to ask the binary for candidates. Invoking it directly here lets us test
// completion logic without a real shell.
func TestClusterIDCompletion(t *testing.T) {
	env := testutil.NewTestEnv(t)

	env.Server.ListClustersCalls.Returns(&clusterv1.ListClustersResponse{
		Items: []*clusterv1.Cluster{
			{Id: "cluster-abc", Name: "my-cluster"},
			{Id: "cluster-xyz", Name: "other-cluster"},
		},
	}, nil)

	stdout, _, err := testutil.Exec(t, env, "__complete", "cluster", "describe", "")
	require.NoError(t, err)
	assert.Contains(t, stdout, "cluster-abc")
	assert.Contains(t, stdout, "my-cluster")
	assert.Contains(t, stdout, "cluster-xyz")
}

